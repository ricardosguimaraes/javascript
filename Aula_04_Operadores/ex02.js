/*Exercício 2
Verifique se as comparações são verdadeiras ou falsas:
- `5 == '5'`
- `5 === '5'`
- `10 != 10'`
- `10 !== '10'`
- `7 > 10`
- `7 <= 7`
*/
console.log(5 == '5');    // true
console.log(5 === '5');   // false
console.log(10 != 10);    // false
console.log(10 !== '10'); // true
console.log(7 > 10);      // false
console.log(7 <= 7);      // true